public class FilaDisciplinaProfessor {

	private String idFila;
	
	private String siape;
	
	private String codigoDisciplina;
	
	private String posicao;
	
	private String prioridade;
	
	private String quantidadeMinistrada;
	
	private String nomeDisciplina;
	
	private String codigoCurso;
	
	private String nomeCurso;
	
	private String nomeProfessor;
	
	private Boolean statusProfessor;

	public String getIdFila() {
		return idFila;
	}

	public void setIdFila(String idFila) {
		this.idFila = idFila;
	}

	public String getSiape() {
		return siape;
	}

	public void setSiape(String siape) {
		this.siape = siape;
	}

	public String getCodigoDisciplina() {
		return codigoDisciplina;
	}

	public void setCodigoDisciplina(String codigoDisciplina) {
		this.codigoDisciplina = codigoDisciplina;
	}

	public String getPosicao() {
		return posicao;
	}

	public void setPosicao(String posicao) {
		this.posicao = posicao;
	}

	public String getPrioridade() {
		return prioridade;
	}

	public void setPrioridade(String prioridade) {
		this.prioridade = prioridade;
	}

	public String getQuantidadeMinistrada() {
		return quantidadeMinistrada;
	}

	public void setQuantidadeMinistrada(String quantidadeMinistrada) {
		this.quantidadeMinistrada = quantidadeMinistrada;
	}

	public String getNomeDisciplina() {
		return nomeDisciplina;
	}

	public void setNomeDisciplina(String nomeDisciplina) {
		this.nomeDisciplina = nomeDisciplina;
	}

	public String getCodigoCurso() {
		return codigoCurso;
	}

	public void setCodigoCurso(String codigoCurso) {
		this.codigoCurso = codigoCurso;
	}

	public String getNomeCurso() {
		return nomeCurso;
	}

	public void setNomeCurso(String nomeCurso) {
		this.nomeCurso = nomeCurso;
	}

	public String getNomeProfessor() {
		return nomeProfessor;
	}

	public void setNomeProfessor(String nomeProfessor) {
		this.nomeProfessor = nomeProfessor;
	}

	public Boolean getStatusProfessor() {
		return statusProfessor;
	}

	public void setStatusProfessor(Boolean statusProfessor) {
		this.statusProfessor = statusProfessor;
	}
	
}
