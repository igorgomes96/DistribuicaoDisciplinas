package br.com.ufu.view;

public class RltTurmasDTO {

	private String codigo;
	private String turma;
	private String nome;
	private String curso;
	private String cargahoraria;
	private String horario;
	private String horarioExtenso;
	

	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public String getTurma() {
		return turma;
	}
	public void setTurma(String turma) {
		this.turma = turma;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCurso() {
		return curso;
	}
	public void setCurso(String curso) {
		this.curso = curso;
	}
	public String getCargahoraria() {
		return cargahoraria;
	}
	public void setCargahoraria(String cargahoraria) {
		this.cargahoraria = cargahoraria;
	}
	public String getHorario() {
		return horario;
	}
	public void setHorario(String horario) {
		this.horario = horario;
	}
	public String getHorarioExtenso() {
		return horarioExtenso;
	}
	public void setHorarioExtenso(String horarioExtenso) {
		this.horarioExtenso = horarioExtenso;
	}

}
